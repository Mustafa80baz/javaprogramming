package day13StringMethods;

public class repeatMethod {
    public static void main(String[] args) {
        ///repeat method

        String str="Cydeo";
       String str1= str.repeat(4);
        System.out.println("str1 = " + str1);
        System.out.println("----------------------------");

        String as="Mustafa BAZ";
        String as1=as.repeat(6);
        System.out.println("as1 = " + as1);
        System.out.println("BAZ.\n".repeat(12));

    }
}
